"use client";

import { useTheme } from "next-themes";

export default function ThemeToggle() {
  const { theme, setTheme } = useTheme();

  return (
    <button
      onClick={() =>
        setTheme(theme === "dark" ? "light" : "dark")
      }
      className="bg-black text-white px-4 py-2 rounded-xl"
    >
      Toggle Theme
    </button>
  );
}